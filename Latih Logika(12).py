#Program Menentukan Huruf Konsonan dan Vokal
while True:
    huruf = input('Silakan masukkan huruf: ')
    print('--------------------------------------')
    
    if huruf == "a" or huruf == 'A':
        print('> Huruf yang Anda masukkan adalah huruf vokal.')
        print('='*22)
        print('='*22)
        
    elif huruf == "i" or huruf == 'I':
        print('> Huruf yang Anda masukkan adalah huruf vokal.')
        print('='*22)
        print('='*22)
        
    elif huruf == 'u' or huruf == 'U':
        print('> Huruf yang Anda masukkan adalah huruf vokal.')
        print('='*22)
        print('='*22)
        
    elif huruf == 'e' or huruf == 'E':
        print('> Huruf yang Anda masukkan adalah huruf vokal.')
        print('='*22)
        print('='*22)
        
    elif huruf == 'o' or huruf == 'O':
        print('> Huruf yang Anda masukkan adalah huruf vokal.')
        print('='*22)
        print('='*22)
        
    else:
        print('> Huruf yang Anda masukkan adalah huruf konsonan.')
